function decode(expr) {

    const binToMorse = (symbol) => {
        const wordMorse = []

        for (let i = 0; i < symbol.length; i += 2) {
            let s = symbol.slice(i, i + 2)

            wordMorse.push(s === '11' ? '-' : s === '10' ? '.' : '')
        }

        return wordMorse.filter(el => el !== '').join('')
    }

    const decoded = []

    for (let i = 0; i < expr.length; i += 10) {
        let s = expr.slice(i, i + 10)

        decoded.push(s === '**********' ? ' ' : MORSE_TABLE[binToMorse(s)])
    }

    return decoded.join('')
}

